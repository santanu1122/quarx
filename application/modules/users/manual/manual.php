    <h1>Users - Module</h1>
        <p>The users module is a simple manager for users that may subscribe to your application/website. You have the ability to add, edit, delete and disable/enable users at will. Also for safety reasons by default all users when they request an account are not instantly activated. Their account requests require you to authorize them.</p>

        <p>However, don't fear, because settings can be adjusted in the settings screen. You can enable instant authentication, or various other settings.</p>
    
